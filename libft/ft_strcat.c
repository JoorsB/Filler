#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;

	i = 0;
	while (*s1)
	{
		s1++;
		i++;
	}
	while (*s2)
	{
		*s1 = *s2;
		s1++;
		s2++;
		i++;
	}
	*s1 = '\0';
	s1 = s1 - i;
	return (s1);
}
